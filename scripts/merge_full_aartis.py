def merge_file(path: Path) -> None:
    data = json.loads(path.read_text(encoding="utf-8"))
    for deity in data.get("deities", []):
        for aarti in deity.get("aartis", []):
            aid = aarti.get("id")
            if aid in AARTIS:
                aarti["versesHi"] = AARTIS[aid]
    path.write_text(
        json.dumps(data, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )


def main() -> None:
    merge_file(CONTENT / "deities.json")
    merge_file(CONTENT / "deities_extra.json")
    # Fix ganesh typo in deities.json if present
    print("Merged full aartis for", len(AARTIS), "deities.")


if __name__ == "__main__":
    main()
